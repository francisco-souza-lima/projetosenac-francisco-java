    private void btnDiviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDiviActionPerformed

        try {
            double a = Double.parseDouble(txtA.getText());
            double b = Double.parseDouble(txtB.getText());

            if (b == 0) {

                JOptionPane.showMessageDialog(null, "Impossivel dividir por zero!", "ERRO!", JOptionPane.ERROR_MESSAGE);
                txtA.requestFocus();
            } else {
                lbnResultado.setText(String.format("%f", op.divi(a, b)));
            }
        } catch (NumberFormatException a) {

            JOptionPane.showMessageDialog(null, "Campos Vazios! \n ERRO: " + a.toString(), "ERRO!", JOptionPane.ERROR_MESSAGE);
            txtA.requestFocus();
        }

    }//GEN-LAST:event_btnDiviActionPerformed

