    private void rbPowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbPowActionPerformed

        try {
            double a = Double.parseDouble(txtA.getText());

            lblResultado.setText(String.format("%.2f", op.pow(a)));
        } catch (NumberFormatException stringVazia) {

            JOptionPane.showMessageDialog(null, "Campos Vazios! \n ERRO: " + stringVazia.toString(), "ERRO!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_rbPowActionPerformed

    private void rbSqrtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbSqrtActionPerformed

        try {

            double a = Double.parseDouble(txtA.getText());

            if (a < 0) {

                lblResultado.setText("Nan");
            } else {

                lblResultado.setText(String.format("%.2f", op.sqrt(a)));
            }

        } catch (NumberFormatException stringVazia) {

            JOptionPane.showMessageDialog(null, "Campos Vazios! \n ERRO: " + stringVazia.toString(), "ERRO!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_rbSqrtActionPerformed

