    private void rbMaxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbMaxActionPerformed

        try {
            double a = Double.parseDouble(txtA.getText());
            double b = Double.parseDouble(txtB.getText());
            double c = Double.parseDouble(txtC.getText());

            lblResultado.setText(String.format("%.2f", op.max(a, b, c)));
        } catch (NumberFormatException stringVazia) {

            JOptionPane.showMessageDialog(null, "Campos Vazios! \n ERRO: " + stringVazia.toString(), "ERRO!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_rbMaxActionPerformed

